#define WCharToUTF8(lpcwszStr,lpszStr,dwSize) \
	{\
	DWORD dwMinSize; \
	dwMinSize = WideCharToMultiByte(CP_UTF8, NULL, lpcwszStr, -1, NULL, 0, NULL, FALSE); \
if (dwSize >= dwMinSize)\
		{\
		WideCharToMultiByte(CP_UTF8, NULL, lpcwszStr, -1, lpszStr, dwSize, NULL, FALSE); \
		}\
}

#define UTF8ToWChar(lpcszStr, lpwszStr,dwSize)  \
	{\
	DWORD dwMinSize; \
	dwMinSize = MultiByteToWideChar(CP_UTF8, 0, lpcszStr, -1, NULL, 0); \
if (dwSize >= dwMinSize)\
		{\
		MultiByteToWideChar(CP_UTF8, 0, lpcszStr, -1, lpwszStr, dwMinSize); \
		}\
}

